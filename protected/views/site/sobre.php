<h4 class="c-grey-900 mB-20">Sobre</h4>
<div class="mT-30">
	<p class="italico">O que é o Portal de Publicações do Programa Mais Médicos (PMM)?</p>
	<p>
		O Portal é uma ferramenta de pesquisa vinculado a Rede Observatório Programa Mais Médicos (ROPMM), que reúne a produção acadêmica em torno desse programa e temas dele derivado. O conjunto de artigos publicados em revistas e periódicos nacionais e estrangeiros aqui reunidos constitui um valioso banco de dados, disponibilizando filtros que permitem acesso rápido à produção sobre o PMM, bem como a realização de estudos sobre essa produção. No Portal pode-se identificar autores e instituições e seus temas e tipos de pesquisa, bem como um conjunto de informações, permitindo a realização de mapas sobre a produção do PMM. 
	</p>
		
	<p class="italico">O Portal de Publicações do Programa Mais Médicos tem por objetivos:
	<ul>
		<li>Identificar e disponibilizar em um só ambiente a produção acadêmica sobre o PMM e temas pertinentes em revistas e periódicos nacionais e internacionais;</li>
		<li>Disponibilizar informações sobre os artigos acadêmicos referente ao PMM, possibilitando a realização, no âmbito do Portal, de pesquisas a partir de dados da publicação (temas, autores, instituições, tipos de pesquisa utilizados, etc);</li>
		<li>Disponibilizar, na forma de representação gráfica, informações sobre a produção acadêmica do PMM e temas pertinentes;</li>
	</ul>
	</p>
	<p class="italico">Metodologia de construção do banco de dados:</p>

	<p>
		Fontes de informação: o banco de dados sobre publicação do PMM foi constituído a partir de sistemas de busca nas principais fontes de publicação e disseminação de produção acadêmica das Ciências da Saúde, como o LILACS, MEDLINE, SCIELO, entre outros.
	</p>
	<br />

	<p class="italico">Organização do Portal - o que você vai encontrar; o que pode pesquisar/buscar:</p>

	<p>O Portal disponibiliza três formas de acesso às informações: artigos, gráficos e palavras-chave. Em cada uma dessas entradas é possível acessar a base de dados de artigos.</p>
	<p>Na aba <b>Artigos</b>, estão listadas todas as publicações, com informações básicas como título, tema da pesquisa/publicação, ano de publicação, autores. Você pode acessar cada um dos artigos, que estão apresentados em tópicos, incluindo o resumo e o link de acesso. Além disso, você pode realizar buscas utilizando-se de variado menu de opções. A aplicação dos filtros resultará em uma lista de artigos com as características escolhidas, bem como ícone de visualização de cada publicação.</p>
	<p>Na aba <b>Gráficos</b> o Portal apresenta algumas composições de dados por temas, ano de publicação, instituições, palavras-chave e tipos de pesquisa. Em cada um desses itens estão reunidas as publicações indicando-se o número, e sua respectiva representação gráfica.</p>
	<p>Na aba <b>Palavras-chave</b> estão disponibilizados os descritores incluídos pelos autores. Você tem acesso a um link no qual pode-se verificar tanto a representação gráfica, como a indicação numérica dos descritores utilizados.</p>

	<p class="italico">Formas de alimentação do Portal:</p>
	<p>A equipe de Projeto ROPMM da UFRGS faz buscas periódicas nas bases de dados e atualiza o Portal.</p>

	<p class="italico">Deseja enviar seu artigo ou tirar dúvidas?</p>
	<p>
		Você pode indicar suas produções, que serão analisadas pela coordenação do Portal e posteriormente inseridas no banco de dados. Entre em contato com a equipe do ROPMM através do e-mail laisc@ufrgs.br
	</p>

	<p><b>Sobre o Programa Mais Médicos:</b></p>
	<p>
		O Programa Mais Médicos (Lei nº 12.871, de 22 de outubro de 2013), constitui-se em política pública do Sistema Único de Saúde que tem por objetivo suprir déficits de médicos no âmbito da Atenção Básica, sobretudo em regiões mais vulneráveis do Brasil, buscando garantir pelo menos um profissional médico em cada município do país e a ampliação da cobertura médica.
	</p>

	<p>O Programa tem como diretrizes:
	<ul>
		<li>Diminuir a carência de médicos nas regiões prioritárias para o SUS, a fim de reduzir as desigualdades regionais na área da Saúde.</li>
		<li>Fortalecer a prestação de serviços de atenção básica em saúde no País. </li>
		<li>Aprimorar a formação médica no País e proporcionar maior experiência no campo de prática médica durante o processo de formação. </li>
		<li>Ampliar a inserção do médico em formação nas unidades de atendimento do SUS, desenvolvendo seu conhecimento sobre a realidade da saúde da população brasileira. </li>
		<li>Fortalecer a política de educação permanente com a integração ensino-serviço, por meio da atuação das instituições de educação superior na supervisão acadêmica das atividades desempenhadas pelos médicos. </li>
		<li>Promover a troca de conhecimentos e experiências entre profissionais de saúde brasileiros e médicos formados em instituições estrangeiras. </li>
		<li>Aperfeiçoar médicos para a atuação nas políticas públicas de saúde do País, na organização e no funcionamento do SUS.</li>
	</ul>
	</p>

	<p>O PMM está organizado em três eixos: </p>
	<p>Eixo 1 - Provimento Emergencial de Médicos</p>
	<p>
		A escassez de médicos formados no Brasil para atender do seu próprio sistema de saúde impôs a necessidade de realização de editais para o provimento emergencial de médicos, enquanto o país não se torne autossuficiente. Médicos intercambistas convocados por meio da cooperação técnica com a OPAS do Brasil, e o estímulo da adesão ao Programa de médicos formados no Brasil, fizeram com que em dezembro de 2017 o Programa Mais Médicos contasse com um total 17.095 médicos no Programa, sendo 50% médicos formados no Brasil e 50% de profissionais intercambistas
	</p>
	<p>
		Atualmente o Programa vagas em 4.058 municípios de todo o país, cobrindo 73% das cidades brasileiras e 34 Distritos Sanitários Especiais Indígenas (DSEIs). Com a expansão do programa o SUS garante assistência para aproximadamente 63 milhões de pessoas.
	</p>

	<p>2º Eixo - Educação </p>
	<p>
		O programa visa também a reestruturação na formação médica do país, buscando, entre outros, fazer frente, em caráter permanente, ao problema da falta de profissionais. Integram o Programa um plano de expansão da graduação e da residência médica e importantes mudanças no modo de formar médicos e também especialistas. A meta é criar 11,5 mil novas vagas de graduação e 12,4 mil vagas de residência até 2017. Destas, já foram autorizadas mais de 5 mil vagas de graduação e quase 5 mil de residência.
	</p>

	<p>3º Eixo – Infraestrutura</p>
	<p>
		Provimento de infraestrutura da Atenção Básica no país, por meio da construção de novas unidades básicas de saúde e reforma e ampliação das unidades já existentes. O Programa prevê investimentos de mais de R$ 5 bilhões para o financiamento de 26 mil obras em quase 5 mil municípios.
	</p>
	
